// Programa para mostrar o resto da divisão
#include <stdio.h>
int main (){

    int num1, num2, resto;
    printf("Digite um número e ENTER\n");       //Número para cálculo
    scanf("%d", &num1);                        //Mostra na tela 

    printf("Digite um número e ENTER\n");      //Número para cálculo
    scanf("%d", &num2);                        //Mostra na tela
    
    resto= num1 % num2;                        //Operação

    printf("O resto da divisão é %d\n", resto); //Mostra o resto

    return 0;







}